package beans;

public class ArticoloAsta {

	public ArticoloAsta() {
		
	}
	
	private int idAsta;
	private int codice;
	
	public int getIdAsta() {
		return this.idAsta;
	}
	
	public void setIdAsta(int idAsta) {
		this.idAsta = idAsta;
	}
	public int getCodice() {
		return this.codice;
	}
	
	public void setCodice(int codice) {
		this.codice = codice;
	}
}
